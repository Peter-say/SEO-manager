    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request , $path = null)
    {
        // dd($request->all());
        $data = $request->validate([
            'file_info' => 'required|mimes:png,jpg',
        ]);

        $path = ImageFile::saveImageRequest($request->file_info, 'ImageFolder', $request);
        $data['file_info'] = $path;
        $data = File::create($data);

        return back()->with('success_message', 'Uploaded successfully');
    }

